#include <string.h>
#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] == s2[i])
			i++;
		if (s1[i] < s2[i] || s1[i] > s2[i])
			return (s1[i] - s2[i]);
	}
	return (0);
}

int	main()
{	

	char *strr1 = "you are so beautiful";
	char *strr2 = "you are s o  beautiful";

	printf("%d \n", strcmp(strr1, strr2));
	printf("%d ", ft_strcmp(strr1, strr2));
}
